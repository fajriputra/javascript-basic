const address = "Jl. Prof. Dr. Hamka";
const age = 19;
const sentence = `Hey I'm ${name} and I'm ${age} years old and My address ${address} `;

const value = `Hey I'm ${name} and I'm ${age} years old and My address ${address}. And here is some simple math ${
  4 * 4 + 3
}`;
console.log(value);

